# Element tags we always strip out — none belong in a static drawing and each
# is an XSS / external-content vector.
_REMOVE_TAGS = {"script", "foreignObject", "iframe", "embed", "object",
                "image", "audio", "video"}


def _local(tag):
    """Local tag/attribute name without its XML namespace (e.g. '{ns}svg' -> 'svg')."""
    return tag.split("}")[-1]


def extract_svg(text):
    """Return just the <svg>...</svg> slice of text, or None if not present."""
    start = text.find("<svg")
    end = text.rfind("</svg>")
    if start == -1 or end == -1:
        return None
    return text[start:end + 6]


def sanitize_svg(text):
    """Extract and clean an SVG so it's safe to inline in the browser.

    Removes disallowed elements, event-handler attributes, off-page hrefs, and
    style attributes that pull in external URLs. Returns the cleaned SVG string,
    or None if no valid SVG could be parsed.
    """
    raw = extract_svg(text)
    if raw is None:
        return None

    # Emit the SVG namespace as the default so output isn't prefixed with ns0:.
    ET.register_namespace("", "http://www.w3.org/2000/svg")
    try:
        root = ET.fromstring(raw)
    except ET.ParseError:
        return None

    def clean(el):
        # Drop disallowed child elements; recurse into the ones we keep.
        for child in list(el):
            if _local(child.tag) in _REMOVE_TAGS:
                el.remove(child)
            else:
                clean(child)
        # Scrub dangerous attributes on this element.
        for name in list(el.attrib):
            local = _local(name).lower()
            value = el.attrib[name]
            if local.startswith("on"):                          # onclick, onload, ...
                del el.attrib[name]
            elif local == "href" and not value.strip().startswith("#"):
                del el.attrib[name]                             # covers href and xlink:href
            elif local == "style" and "url(" in value.lower():
                del el.attrib[name]

    clean(root)
    return ET.tostring(root, encoding="unicode")



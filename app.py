def _validate_images(images):
    """Validate the images list. Returns an error string, or None if OK."""
    if not isinstance(images, list) or not (1 <= len(images) <= config.MAX_IMAGES):
        return f"Provide 1 to {config.MAX_IMAGES} photo(s)."
    for b64 in images:
        if not isinstance(b64, str) or not b64.strip():
            return "Each image must be a non-empty base64 string."
        if b64.strip().startswith("data:"):
            return "Strip the 'data:...;base64,' prefix — send only the base64 data."
    return None


def _validate_scale(raw, n_images):
    """Validate the optional `scale` dict. Returns (scale, error): on success
    scale is the cleaned dict (or None if not provided) and error is None; on
    failure scale is None and error is a (response, status) tuple.
    """
    if raw is None:
        return None, None
    if not isinstance(raw, dict):
        return None, (jsonify({"error": "scale must be an object."}), 400)
    px_per_mm = raw.get("px_per_mm")
    if isinstance(px_per_mm, bool) or not isinstance(px_per_mm, (int, float)) or px_per_mm <= 0:
        return None, (jsonify({"error": "scale.px_per_mm must be a number greater than 0."}), 400)
    marker_photo = raw.get("marker_photo")
    if isinstance(marker_photo, bool) or not isinstance(marker_photo, int) or not (0 <= marker_photo < n_images):
        return None, (jsonify({"error": "scale.marker_photo is out of range."}), 400)
    marker_size_mm = raw.get("marker_size_mm")
    if isinstance(marker_size_mm, bool) or not isinstance(marker_size_mm, (int, float)) or marker_size_mm <= 0:
        return None, (jsonify({"error": "scale.marker_size_mm must be a number greater than 0."}), 400)
    return ({"px_per_mm": float(px_per_mm), "marker_photo": marker_photo,
             "marker_size_mm": marker_size_mm}, None)


def _validate_drawing_inputs(data):
    """Validate fields shared by /api/generate and /api/refine. Returns
    (parsed, error): parsed is a dict of cleaned values on success (error None),
    or None with error = (response, status) on failure.
    """
    images = data.get("images")
    err = _validate_images(images)
    if err:
        return None, (jsonify({"error": err}), 400)

    # dimensions: 1..MAX_DIMENSIONS entries of {"label": str, "value": number>0}.
    raw_dims = data.get("dimensions")
    if not isinstance(raw_dims, list) or not (1 <= len(raw_dims) <= config.MAX_DIMENSIONS):
        return None, (jsonify({"error": f"Provide 1 to {config.MAX_DIMENSIONS} dimensions."}), 400)
    dimensions = []
    for d in raw_dims:
        if not isinstance(d, dict):
            return None, (jsonify({"error": "Each dimension must be an object."}), 400)
        label = d.get("label")
        if not isinstance(label, str) or not label.strip():
            return None, (jsonify({"error": "Each dimension needs a non-empty label."}), 400)
        value = d.get("value")
        if isinstance(value, bool) or not isinstance(value, (int, float)) or value <= 0:
            return None, (jsonify({"error": "Each dimension value must be a number greater than 0."}), 400)
        dimensions.append({"label": label.strip(), "value": float(value)})

    unit = data.get("unit", "mm")
    if unit not in ("mm", "cm", "in"):
        return None, (jsonify({"error": "unit must be 'mm', 'cm', or 'in'."}), 400)

    description = data.get("description")
    if description is not None and not isinstance(description, str):
        return None, (jsonify({"error": "description must be text."}), 400)

    quality = data.get("quality", "fast")
    if quality not in ("draft", "fast", "best"):
        return None, (jsonify({"error": "quality must be 'draft', 'fast', or 'best'."}), 400)
    model = {"draft": config.MODEL_DRAFT,
             "fast": config.MODEL_FAST,
             "best": config.MODEL_BEST}[quality]

    scale, err = _validate_scale(data.get("scale"), len(images))
    if err:
        return None, err

    return ({"images": images, "dimensions": dimensions, "unit": unit,
             "description": description, "quality": quality, "model": model,
             "scale": scale}, None)


def _build_stream_kwargs(quality):
    """Assemble Claude stream options for a quality tier.

    Haiku 4.5 (draft) does not support adaptive thinking, so thinking is only
    sent to Sonnet/Opus. The Fast (Sonnet) tier also runs effort "medium" —
    output/thinking tokens are most of the cost, and medium ~matches the old
    default at lower spend. Best (Opus) keeps the default high effort.
    """
    stream_kwargs = {}
    if quality != "draft":
        stream_kwargs["thinking"] = {"type": "adaptive"}
    if quality == "fast":
        stream_kwargs["output_config"] = {"effort": "medium"}
    return stream_kwargs



def render_svg_to_png(svg_text):
    """Rasterize an SVG sheet to PNG bytes with resvg (Rust, no system deps —
    the researched safe choice for Render; cairosvg is a known deploy trap).
    resvg loads NO system fonts by itself, so we bundle DejaVu Sans in
    static/fonts and point it there — without this all dimension text would
    silently vanish from the render."""
    import resvg_py  # lazy so stub/local dev needn't install it

    font_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                            "static", "fonts")
    png = resvg_py.svg_to_bytes(
        svg_string=svg_text,
        background="#ffffff",
        skip_system_fonts=True,          # deterministic: bundled font only
        font_dirs=[font_dir],
        font_family="DejaVu Sans",
        sans_serif_family="DejaVu Sans",
        width=1600,
    )
    return bytes(png)


# The critique instruction shown to the model along with the rendered sheet.
REVIEW_INSTRUCTION = (
    "This is a render of the engineering drawing you produced for the "
    "photographed part. List up to 5 concrete visual defects (overlapping "
    "text, colliding views, missing/broken lines, elements outside the sheet, "
    "gross proportion errors vs the photo). If there are none, reply "
    "DEFECTS: NONE. Then output the corrected content SVG per the same rules, "
    "changing ONLY what fixes the listed defects."
)


def _parse_review_reply(text):
    """Split the review reply into (defects, content_svg).

    Expected shape: "DEFECTS: NONE" alone, or "DEFECTS:" + up to 5 "- ..."
    lines + one corrected content SVG. Returns ([], None) for the NONE case;
    (defects, None) means a defect list arrived without a usable SVG.
    """
    head = text.split("<svg", 1)[0]
    if "DEFECTS: NONE" in head.upper().replace("  ", " "):
        return [], None
    defects = []
    seen_marker = False
    for line in head.splitlines():
        stripped = line.strip()
        if stripped.upper().startswith("DEFECTS"):
            seen_marker = True
            continue
        if seen_marker and stripped:
            # Strip list bullets/numbers: "- x", "* x", "1. x", "2) x"
            cleaned = stripped.lstrip("-*").strip()
            if cleaned[:2].rstrip(".)").isdigit():
                cleaned = cleaned.lstrip("0123456789.)").strip()
            if cleaned:
                defects.append(cleaned)
    return defects[:5], extract_svg(text)


def make_review_finalizer(meta):
    """Finalizer for /api/review: parse the defect list + corrected content,
    compose the corrected sheet, and emit done {"svg", "defects"}.
    A clean bill of health emits done {"svg": null, "defects": []}."""
    def finalize(text):
        defects, content = _parse_review_reply(text)
        if not defects:
            yield _ndjson({"type": "done", "svg": None, "defects": []})
            return
        if content is None:
            print(f"[BluePrint] review reply had defects but no SVG: {text[:200]!r}")
            yield _ndjson({"type": "error",
                           "message": "The review reply was malformed — try again."})
            return
        try:
            composed = sheet_compose.compose_sheet(content, meta)
        except ValueError as exc:
            print(f"[BluePrint] review compose failed: {exc}")
            yield _ndjson({"type": "error",
                           "message": "The review reply was malformed — try again."})
            return
        cleaned = sanitize_svg(composed)
        if cleaned is None:
            yield _ndjson({"type": "error",
                           "message": "The review reply was malformed — try again."})
            return
        yield _ndjson({"type": "done", "svg": cleaned, "defects": defects})
    return finalize


@app.route("/api/review", methods=["POST"])
@login_required
def api_review():
    data = request.get_json(silent=True) or {}
    parsed, error = _validate_drawing_inputs(data)
    if error:
        return error

    svg = data.get("svg")
    if not isinstance(svg, str) or "<svg" not in svg:
        return jsonify({"error": "svg must be a string containing '<svg'."}), 400
    if len(svg.encode("utf-8")) > 2 * 1024 * 1024:
        return jsonify({"error": "SVG is too large (max 2 MB)."}), 413

    if not config.STUB_MODE and not config.ANTHROPIC_API_KEY:
        return jsonify({"error": "not_configured"}), 503

    quality = parsed["quality"]
    model = parsed["model"]
    meta = {"description": parsed["description"], "unit": parsed["unit"]}

    # REVIEW-INPUT DESIGN: the client sends the COMPOSED sheet it already holds
    # (same shape as refine turns — no second svg format on the wire). The model
    # sees two things: the rasterized PNG of that composed sheet (the external
    # ground truth it must critique) and, as its own prior assistant turn, the
    # CONTENT fragment extracted from the sheet — so it revises exactly what it
    # authored, and the composed-vs-content split stays invisible to the client.
    fragment = None
    if not config.STUB_MODE:
        fragment = sheet_compose.extract_content_fragment(svg)
        if fragment is None:
            return jsonify({"error": "old_drawing",
                            "message": "This drawing predates server-composed sheets — "
                                       "generate a new drawing to review."}), 422

    def generate():
        yield _ndjson({"type": "status", "message": "Reviewing the drawing..."})

        # ---- Stub branch: canned defects + the input sheet, stamped ----
        if config.STUB_MODE:
            for i in range(2):
                time.sleep(0.4)
                yield _ndjson({"type": "progress", "chars": (i + 1) * 1000})
            stamp = ('<text x="800" y="30" font-family="sans-serif" font-size="20" '
                     'fill="black" text-anchor="middle">REVIEWED (STUB)</text>')
            yield _ndjson({"type": "done",
                           "svg": svg.replace("</svg>", stamp + "</svg>"),
                           "defects": ["Dimension text overlaps the front view (stub)",
                                       "Top view not aligned with front view (stub)"]})
            return

        # ---- Real branch: rasterize, then ask the model to critique it ----
        try:
            png_b64 = base64.b64encode(render_svg_to_png(svg)).decode("ascii")
        except Exception as exc:
            print(f"[BluePrint] review rasterize failed: {exc!r}")
            yield _ndjson({"type": "error",
                           "message": "Could not render the drawing for review."})
            return

        messages = [
            {   # byte-identical first message — shares the prompt-cache prefix
                "role": "user",
                "content": build_first_user_message(
                    parsed["images"], parsed["dimensions"], parsed["unit"],
                    parsed["description"], parsed["scale"]),
            },
            {   # the model's own authored content, reconstructed
                "role": "assistant",
                "content": fragment,
            },
            {   # the critique turn: rendered sheet + first source photo + text
                "role": "user",
                "content": [
                    {"type": "image",
                     "source": {"type": "base64", "media_type": "image/png",
                                "data": png_b64}},
                    {"type": "image",
                     "source": {"type": "base64", "media_type": "image/jpeg",
                                "data": parsed["images"][0]}},
                    {"type": "text", "text": REVIEW_INSTRUCTION,
                     "cache_control": {"type": "ephemeral"}},
                ],
            },
        ]
        yield from stream_drawing(model, messages, _build_stream_kwargs(quality),
                                  make_review_finalizer(meta))

    return Response(
        stream_with_context(generate()),
        mimetype="application/x-ndjson",
        headers={"Cache-Control": "no-cache", "X-Accel-Buffering": "no"},
    )



def build_first_user_message(images, dimensions, unit, description, scale):
    """Build the FIRST user message content (image blocks first, then one text
    block). This must be byte-identical between /api/generate, /api/refine AND
    /api/review so it forms a stable prompt-cache prefix — the cache_control
    breakpoint lives on its final text block. `dimensions` is a list of
    {"label", "value"} dicts (1..MAX_DIMENSIONS); `scale` is None or a dict with
    px_per_mm, marker_photo, and marker_size_mm from ArUco detection.
    """
    dims_text = "; ".join(f"{d['label']} = {d['value']} {unit}" for d in dimensions)
    lines = [
        f"Object: {description or 'not described'}.",
        f"Known dimensions (ALL authoritative, mark each with * on the sheet, "
        f"NEVER snap them): {dims_text}.",
        f"Use {unit} as the unit for EVERY dimension on the sheet.",
        f"Photos are attached in order as Image 1..{len(images)}.",
        f'Draw your content SVG with viewBox="{sheet_compose.CONTENT_VIEWBOX}" — '
        f"this is the content area only; the server composes the sheet border, "
        f"title block, isometric view and detail views around it.",
    ]
    if scale is not None:
        marker_size_mm = scale["marker_size_mm"]
        px_per_mm = scale["px_per_mm"]
        px = px_per_mm * marker_size_mm
        lines.append(
            f"Photo {scale['marker_photo'] + 1} contains a printed ArUco scale "
            f"marker (outlined and labeled in the image). Measured calibration "
            f"for that photo: {marker_size_mm} mm = {px} px, "
            f"i.e. {px_per_mm:.2f} px/mm."
        )
    lines.append("Generate the engineering drawing content now.")

    content = [
        {"type": "image",
         "source": {"type": "base64", "media_type": "image/jpeg", "data": b64}}
        for b64 in images
    ]
    content.append({
        "type": "text",
        "text": "\n".join(lines),
        "cache_control": {"type": "ephemeral"},  # cache prefix breakpoint
    })
    return content


def stream_drawing(model, messages, stream_kwargs, finalize):
    """Stream one model reply from Claude as NDJSON lines. Shared by generate,
    refine and review. The Claude stream is consumed in a background thread that
    pushes char-counts into a queue; this generator polls with a 5-second timeout
    so heartbeats fire on wall-clock time even during a long silent thinking phase
    (a silent HTTP response would get killed by Render's proxy).

    `finalize` is a generator function called with the model's final text; it
    yields the closing NDJSON line(s) (done or error). Generate/refine use
    make_compose_finalizer; review uses its own defect-parsing finalizer.
    """
    import anthropic  # imported lazily so stub/local dev needn't install it

    client = anthropic.Anthropic(api_key=config.ANTHROPIC_API_KEY)
    events_q = queue.Queue()
    outcome = {}  # "msg" on success, "exc" on failure (set by the worker)

    def consume_stream():
        try:
            with client.messages.stream(
                model=model,
                max_tokens=64000,
                **stream_kwargs,
                system=SYSTEM_PROMPT,
                messages=messages,
            ) as stream:
                for event in stream:
                    if (getattr(event, "type", "") == "content_block_delta"
                            and getattr(event.delta, "type", None) == "text_delta"):
                        events_q.put(len(event.delta.text))
                outcome["msg"] = stream.get_final_message()
        except Exception as exc:  # reported to the client by the generator below
            outcome["exc"] = exc
        finally:
            events_q.put(None)  # sentinel: stream finished (ok or not)

    worker = threading.Thread(target=consume_stream, daemon=True)
    worker.start()

    total_chars = 0
    last_progress_mark = 0
    while True:
        try:
            item = events_q.get(timeout=5)
        except queue.Empty:
            yield _ndjson({"type": "heartbeat"})
            continue
        if item is None:
            break
        total_chars += item
        if total_chars - last_progress_mark >= 2000:
            last_progress_mark = total_chars
            yield _ndjson({"type": "progress", "chars": total_chars})

    exc = outcome.get("exc")
    if exc is not None:
        if isinstance(exc, anthropic.AuthenticationError):
            yield _ndjson({"type": "error", "message": "Server API key is invalid."})
        elif isinstance(exc, anthropic.RateLimitError):
            yield _ndjson({"type": "error", "message": "The AI is busy — try again in a minute."})
        elif isinstance(exc, anthropic.APIStatusError):
            print(f"[BluePrint] APIStatusError {exc.status_code}: {exc.message}")
            yield _ndjson({"type": "error", "message": "Something went wrong talking to the AI — try again."})
        elif isinstance(exc, anthropic.APIConnectionError):
            yield _ndjson({"type": "error", "message": "Network error talking to the AI."})
        else:
            print(f"[BluePrint] unexpected error: {exc!r}")
            yield _ndjson({"type": "error", "message": "Something went wrong generating the drawing — try again."})
        return
    msg = outcome["msg"]

    stop = msg.stop_reason
    # flush=True so token counts appear in the log immediately (stdout is
    # block-buffered when redirected to a file, e.g. on Render). Cache token
    # counts are guarded — older SDKs/models may not report them.
    cache_creation = getattr(msg.usage, "cache_creation_input_tokens", 0) or 0
    cache_read = getattr(msg.usage, "cache_read_input_tokens", 0) or 0
    print(f"[BluePrint] model={model} input_tokens={msg.usage.input_tokens} "
          f"output_tokens={msg.usage.output_tokens} "
          f"cache_creation_input_tokens={cache_creation} "
          f"cache_read_input_tokens={cache_read} stop_reason={stop}", flush=True)

    if stop == "end_turn":
        # Final text comes ONLY from text blocks (skip thinking blocks).
        text = "".join(b.text for b in msg.content if b.type == "text")
        yield from finalize(text)
    elif stop == "max_tokens":
        yield _ndjson({"type": "error",
                       "message": "The drawing ran out of space — try fewer photos or a simpler object."})
    elif stop == "refusal":
        details = getattr(msg, "stop_details", None)  # may be None
        category = getattr(details, "category", None) if details else None
        print(f"[BluePrint] refusal category={category}")
        yield _ndjson({"type": "error", "message": "The model declined this request."})
    else:
        print(f"[BluePrint] unexpected stop_reason={stop}")
        yield _ndjson({"type": "error", "message": "Something went wrong generating the drawing — try again."})


def make_compose_finalizer(meta):
    """Finalizer for generate/refine: the model's reply is a CONTENT fragment;
    compose the full sheet around it, sanitize the COMPOSED sheet, and emit it.
    """
    def finalize(text):
        content = extract_svg(text)
        if content is None:
            print(f"[BluePrint] no SVG in model reply, raw prefix: {text[:200]!r}")
            yield _ndjson({"type": "error",
                           "message": "The model returned a malformed drawing — try again."})
            return
        try:
            composed = sheet_compose.compose_sheet(content, meta)
        except ValueError as exc:
            print(f"[BluePrint] compose failed: {exc}")
            yield _ndjson({"type": "error",
                           "message": "The model returned a malformed drawing — try again."})
            return
        cleaned = sanitize_svg(composed)
        if cleaned is None:
            print("[BluePrint] composed sheet failed sanitizing")
            yield _ndjson({"type": "error",
                           "message": "The model returned a malformed drawing — try again."})
            return
        yield _ndjson({"type": "done", "svg": cleaned})
    return finalize



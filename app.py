@app.url_defaults
def static_cache_bust(endpoint, values):
    """Append each static file's mtime as ?v=... so browsers (and the service
    worker's runtime cache) fetch fresh copies whenever a file changes —
    without this, cached JS/CSS can outlive a deploy and break the page."""
    if endpoint == "static" and "filename" in values:
        path = os.path.join(app.static_folder, values["filename"])
        try:
            values["v"] = int(os.path.getmtime(path))
        except OSError:
            pass



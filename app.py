@app.route("/api/refine", methods=["POST"])
@login_required
def api_refine():
    data = request.get_json(silent=True) or {}
    parsed, error = _validate_drawing_inputs(data)
    if error:
        return error

    # turns: 1..MAX_REFINE_TURNS prior [{instruction, svg}] entries. Each svg must
    # look like an SVG; only turn 0's instruction may be empty (it's the original
    # generate, which had no instruction).
    turns = data.get("turns")
    if not isinstance(turns, list) or not (1 <= len(turns) <= config.MAX_REFINE_TURNS):
        return jsonify({"error": f"turns must have 1 to {config.MAX_REFINE_TURNS} entries."}), 400
    for i, turn in enumerate(turns):
        if not isinstance(turn, dict):
            return jsonify({"error": "Each turn must be an object."}), 400
        svg = turn.get("svg")
        if not isinstance(svg, str) or "<svg" not in svg:
            return jsonify({"error": "Each turn must include an svg containing '<svg'."}), 400
        instr = turn.get("instruction")
        if not isinstance(instr, str):
            return jsonify({"error": "Each turn instruction must be text."}), 400
        if i > 0 and not instr.strip():
            return jsonify({"error": "Only turn 0 may have an empty instruction."}), 400

    instruction = data.get("instruction")
    if not isinstance(instruction, str) or not instruction.strip():
        return jsonify({"error": "An instruction is required."}), 400

    if not config.STUB_MODE and not config.ANTHROPIC_API_KEY:
        return jsonify({"error": "not_configured"}), 503

    quality = parsed["quality"]
    model = parsed["model"]
    meta = {"description": parsed["description"], "unit": parsed["unit"]}

    # REFINE-HISTORY DESIGN: the client stores and sends COMPOSED sheets in
    # `turns` (that's what the user sees, saves to the gallery, and re-sends —
    # one svg shape everywhere). But the model must revise what IT authored, so
    # the server extracts each turn's CONTENT fragment out of the composed sheet
    # (sheet_compose.extract_content_fragment — deterministic, so identical
    # sheets always extract to identical fragments and the prompt-cache prefix
    # stays stable across refine turns) and sends the fragments back as the
    # assistant turns. build_first_user_message stays byte-identical with
    # /api/generate, so the big image-bearing first message keeps caching.
    fragments = []
    if not config.STUB_MODE:
        for turn in turns:
            frag = sheet_compose.extract_content_fragment(turn["svg"])
            if frag is None:
                return jsonify({"error": "old_drawing",
                                "message": "This drawing predates server-composed sheets — "
                                           "generate a new drawing to refine."}), 422
            fragments.append(frag)

    def generate():
        yield _ndjson({"type": "status", "message": "Applying your changes..."})

        # ---- Stub branch: echo the instruction into the sample content, then
        # run the REAL composer so the output is a full composed sheet ----
        if config.STUB_MODE:
            for i in range(2):
                time.sleep(0.4)
                yield _ndjson({"type": "progress", "chars": (i + 1) * 1000})
            note = (f'<text x="20" y="70" font-family="sans-serif" font-size="20" '
                    f'fill="black">REFINED (STUB): {escape(instruction)}</text>')
            content = SAMPLE_CONTENT_SVG.replace("</svg>", note + "</svg>")
            composed = sheet_compose.compose_sheet(content, _stub_meta())
            yield _ndjson({"type": "done", "svg": sanitize_svg(composed)})
            return

        # ---- Real branch: rebuild the conversation and ask for a revision ----
        # user(first message) / assistant(turn0 content) / user(turn1 instr) /
        # assistant(turn1 content) / ... / user(new instruction). Turn 0's empty
        # instruction is NOT sent — it was the original generate.
        messages = [{
            "role": "user",
            "content": build_first_user_message(
                parsed["images"], parsed["dimensions"], parsed["unit"],
                parsed["description"], parsed["scale"]),
        }]
        for i, frag in enumerate(fragments):
            if i > 0:
                messages.append({"role": "user", "content": turns[i]["instruction"]})
            messages.append({"role": "assistant", "content": frag})
        messages.append({"role": "user", "content": [
            {"type": "text", "text": instruction, "cache_control": {"type": "ephemeral"}},
        ]})

        yield from stream_drawing(model, messages, _build_stream_kwargs(quality),
                                  make_compose_finalizer(meta))

    return Response(
        stream_with_context(generate()),
        mimetype="application/x-ndjson",
        headers={"Cache-Control": "no-cache", "X-Accel-Buffering": "no"},
    )


